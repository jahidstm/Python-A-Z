my_numbers = [10, 20, 30, 40, 50]
print("Original List:")
print(my_numbers)

# Slicing
sliced_part = my_numbers[1:4]
print("Sliced Part:")
print(sliced_part)

# Insert
my_numbers.insert(2, 25)
print("After Insert:")
print(my_numbers)

# Delete
del my_numbers[0]
print("After Delete:")
print(my_numbers)